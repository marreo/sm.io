function arrayToPath(correctPath) {
  //First object M
  correctPath[0][Object.keys(correctPath[0])[0]] = 'M';
  //Last object L
  correctPath[correctPath.length-1][Object.keys(correctPath[correctPath.length-1])[0]] = 'L';

  var pathString = "";
  for (var i = 0; i < correctPath.length; i++) {
    for (var j = 0; j < correctPath[i].length; j++) {
      pathString += correctPath[i][j] + " ";
    }
  }
  return pathString;
}

function isOverlapping(facitElement, element) {
  //left, top, x(left + width), y(top + height)
  var brushWidth = 50,
    brushHeight = 50,
    d1x = facitElement.left,
    d1y = facitElement.top,
    d1xMax = facitElement.left + brushWidth,
    d1yMax = facitElement.top + brushHeight,
    d2x = element.left,
    d2y = element.top,
    d2xMax = element.left + brushWidth,
    d2yMax = element.top + brushHeight,
    x_overlap = Math.max(0, Math.min(d1xMax, d2xMax) - Math.max(d1x, d2x)),
    y_overlap = Math.max(0, Math.min(d1yMax, d2yMax) - Math.max(d1y, d2y)),
    total_overlap = x_overlap * y_overlap;
  return total_overlap > 0;
}